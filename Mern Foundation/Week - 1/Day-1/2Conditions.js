// Here we will explore different types of conditions in JavaScript
// 1. If-Else Statement

let age = 20;
if (age < 18) {
    console.log("You are a minor.");
} else {
    console.log("You are an adult.");
}

// 2. Else If Ladder
let score = 85;
if (score >= 90) {
    console.log("Grade: A");
} else if (score >= 80) {
    console.log("Grade: B");
}
else if (score >= 70) {
    console.log("Grade: C");
}   
else {
    console.log("Grade: F");
}