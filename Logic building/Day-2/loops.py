# What is loop 
# Loops are used to repeat instruction / piece of code untill a certain condition is met 
# Loop is a programming construct that allows for repeated execution of a block of code as long as a specified condition is met.
# In Python, there are two main types of loops: for loops and while loops.
# A for loop is used for iterating over a sequence (such as a list, tuple, or string) or other iterable objects.
# A while loop repeatedly executes a block of code as long as a specified condition is true.